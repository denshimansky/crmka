-- Маркер источника появления контакта.
--
-- Проблема: массовый импорт исторической базы (CSV/XLSX и синхронизация лидов
-- из 1С) создаёт записи Client с createdAt = момент импорта. Из-за этого вся
-- залитая база попадала в этап «Лид» воронки продаж и в отчёт «Лиды по
-- менеджерам» как «новые лиды месяца» (см. docs/reports-logic.md, CRM-13).
--
-- Решение: явный маркер source. Импорт-пути проставляют 'import' (см.
-- api/clients/import/route.ts и lib/leads-import/sync-leads.ts); обычное
-- создание (быстрый «+», quick-create, портал, сидер) — 'manual' по умолчанию.
-- Воронка и отчёты по лидам исключают source='import' из подсчёта новых лидов.
--
-- Бэкафилл уже импортированных контактов делается отдельно, адресно на сервере
-- (точные тенант+минута пакетного импорта) — НЕ в миграции, чтобы случайно не
-- задеть сид/тестовые данные с близкими created_at.

CREATE TYPE "ClientSource" AS ENUM ('manual', 'import');

ALTER TABLE "clients"
  ADD COLUMN "source" "ClientSource" NOT NULL DEFAULT 'manual';
