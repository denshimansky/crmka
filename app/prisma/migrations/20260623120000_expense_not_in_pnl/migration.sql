-- Новый режим признания расхода: not_in_pnl — расход не попадает в ОПИУ/финрез
-- (только в ДДС). Используется для выводов средств, возврата тела займа, покупки
-- активов и т.п. expandExpenseToMonths возвращает [] для таких расходов, поэтому
-- они автоматически исключаются из всех ОПИУ-отчётов; отчёты с прямым суммированием
-- (рентабельность педагогов, прогноз прибыли, ОПИУ по направлениям) фильтруют
-- recognition_mode <> 'not_in_pnl'.
ALTER TYPE "ExpenseRecognitionMode" ADD VALUE IF NOT EXISTS 'not_in_pnl';
