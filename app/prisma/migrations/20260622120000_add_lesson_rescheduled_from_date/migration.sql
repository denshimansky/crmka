-- Исходная (первая) дата занятия до переноса.
-- NULL = занятие не переносили (или перенос произошёл до этой миграции — историю
-- восстановить нельзя, такие занятия считаются «не переносившимися»: состав
-- считается по текущей date, как раньше). Бэкафилл не делаем намеренно.
--
-- Зачем: при переносе занятия PATCH /api/lessons/[id] перезаписывал date новой
-- датой, и состав занятия (rosterWhereOnDate / Subscription.startDate <= date /
-- enrolledAt <= date) начинал считаться по НОВОЙ дате. Из-за этого ученик,
-- начавший заниматься ПОЗЖЕ исходной даты, но не позже новой, ошибочно попадал в
-- перенесённое занятие (и мог быть списан при «Отметить всех»). Теперь состав и
-- резолв абонемента считаются по rescheduled_from_date ?? date.

ALTER TABLE "lessons" ADD COLUMN "rescheduled_from_date" DATE;
