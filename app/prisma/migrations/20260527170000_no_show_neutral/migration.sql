-- «Не был» — нейтральный промежуточный статус.
-- Раньше списывал с абонемента и считался выручкой, что создавало
-- «качели» баланса: оператор поставил «Не был» → списалось → потом
-- поменял на «Уваж. пропуск» → вернулось → и т.п.
-- Теперь «Не был» ничего не двигает; деньги и ЗП трогаются только
-- финальными статусами (Был / Прогул / УП / Отработка).
--
-- Существующие отметки в БД не пересчитываем: их chargeAmount уже
-- зафиксирован в `Attendance` и в `Subscription.balance`. Пересчёт
-- сейчас исказил бы балансы, у которых уже была история операций.
-- Корректность будущих отметок обеспечивается флагами AttendanceType.

UPDATE "attendance_types"
SET
  "charges_subscription" = FALSE,
  "counts_as_revenue"    = FALSE,
  "pays_instructor"      = FALSE
WHERE code = 'no_show'
  AND tenant_id IS NULL;
