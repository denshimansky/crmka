-- Технический флаг для разовых занятий вне расписания группы.
-- Группа с is_one_time=true создаётся под единичный Lesson (например,
-- индивидуальное занятие, приехавший на разовое посещение и т.п.).
-- Такие группы скрываются из списков групп и отчётов с группировкой
-- по группам, чтобы не засорять интерфейс «одноразовыми» сущностями.

ALTER TABLE "groups"
ADD COLUMN "is_one_time" BOOLEAN NOT NULL DEFAULT FALSE;
